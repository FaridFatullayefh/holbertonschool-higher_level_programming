#!/usr/bin/env python3
"""Module defining an abstract class Animal and its subclasses."""
from abc import ABC, abstractmethod


class Animal(ABC):
    """Abstract base class representing an animal."""

    @abstractmethod
    def sound(self):
        """Abstract method that must be implemented by subclasses."""
        pass


class Dog(Animal):
    """Subclass of Animal representing a dog."""

    def sound(self):
        """Implementation of the sound method for a dog."""
        return "Bark"


class Cat(Animal):
    """Subclass of Animal representing a cat."""

    def sound(self):
        """Implementation of the sound method for a cat."""
        return "Meow"
